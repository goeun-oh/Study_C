#include <stdio.h>

int main(){
    // 동일한 값을 float와 double에 저장
    float ft = 1.234567890123456789;
    double db = 1.234567890123456789;

    // 소수점 이하 20자리까지 출력하여 정밀도 차이 확인
    printf("float형 변수의 값: %.20f\n", ft);
    printf("double형 변수의 값: %.20lf\n", db);

    return 0;
}

/*
실행 결과:
float형 변수의 값: 1.23456788063049316406
double형 변수의 값: 1.23456789012345669043

분석:
- float: 단정밀도(4바이트) - 약 7자리 정밀도
- double: 배정밀도(8바이트) - 약 15자리 정밀도
- 컴퓨터 구조상 정확한 소수점 표현이 어려워 근사값으로 저장됨
- IEEE 754 표준을 따르는 부동소수점 표현 방식 사용
*/